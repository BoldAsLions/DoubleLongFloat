public class DoubleLongFloat {
    public static void main(String[] args) {

        int a = 9;
        long b = 99999999L;
        float c = 2.5f;
        double d = 343433.34343;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
